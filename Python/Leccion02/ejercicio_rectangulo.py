# Ejercicio rectangulo

alto = int(input('Cúal es el alto del rectangulo: '))
ancho = int(input('Cúal es el ancho del rectangulo: '))

print('Proporciona el alto del rectangulo:', alto)
print('Proporciona el ancho del rectangulo:', ancho)

area = alto * ancho
perimetro = (alto + ancho) * 2

print('Area:', area)
print('Perimetro:', perimetro)
