# operador de asignación "="

mi_variable = 10
print(mi_variable)

# se reasigna la variable + 1 para la principal que teníamos

mi_variable = mi_variable + 1
print(mi_variable)

# incremento con reasignación

mi_variable += 1
print(mi_variable)

#reasignación con otro operador (resta mi_variable = mi_variable - 2)
mi_variable -= 2
print(mi_variable)

#reasignación con otro operador (multiplicación mi_variable = mi_variable * 3)
mi_variable *= 3
print(mi_variable)

#division reasignación (división mi_variable = mi_variable / 2)
mi_variable /= 2
print(mi_variable)

